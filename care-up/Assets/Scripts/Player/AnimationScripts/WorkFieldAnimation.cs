using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class WorkFieldAnimation : CinematicAnimation
{
    HandsInventory inv;
    WorkField wf;

    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    public override void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        base.OnStateEnter(animator, stateInfo, layerIndex);

        inv = GameObject.Find("GameLogic").GetComponent<HandsInventory>();
        inv.CreateAnimationObject("Cloth", false);

        wf = GameObject.Find("WorkField").GetComponent<WorkField>();
    }

    // OnStateUpdate is called on each Update frame between OnStateEnter and OnStateExit callbacks
    //override public void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex) {
    //
    //}

    // OnStateExit is called when a transition ends and the state machine finishes evaluating this state
    public override void OnStateExit(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        base.OnStateExit(animator, stateInfo, layerIndex);

        inv.DeleteAnimationObject();
        wf.ToggleObjects();
    }

